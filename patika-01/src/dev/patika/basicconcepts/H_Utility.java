package dev.patika.basicconcepts;

public class H_Utility {
    /*
     The Utility class is a class that defines common methods that more than one class can use.
     The best example of this is the java.lang.math class.
     Within this class, common mathematical methods that each class can use are defined.
     It doesn't make sense to just create an object of classes that contain utility methods.
     These classes have no state, so there are no variables defined in the class, only methods.
     These methods are operations grouped under the utility class and
     perform a task for the object of the calling class,
     not the object of the class to which they belong.
    */
}
